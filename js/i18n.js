let currentLang = localStorage.getItem('lang') || 'ru';

function applyTranslations(lang) {
  const t = translations[lang];
  if (!t) return;
  
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (t[key]) {
      if (el.dataset.i18nHtml) {
        el.innerHTML = t[key];
      } else {
        el.textContent = t[key];
      }
    }
  });
  
  const latinRoles = ['Backend Developer', 'Python Engineer', 'Go Enthusiast', 'Security Researcher'];
  window.typedRoles = latinRoles.map((fallback, i) => t[`hero-role-${i + 1}`] || fallback);

  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  localStorage.setItem('lang', lang);
  currentLang = lang;

  if (window.restartTyped) window.restartTyped(window.typedRoles);

  if (window['rerenderProjects']) window['rerenderProjects']();
}

function initLangSwitcher() {
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', (e) => {
      e.preventDefault();
      const lang = btn.dataset.lang;
      if (lang !== currentLang) {
        applyTranslations(lang);
      }
    });
  });
  
  applyTranslations(currentLang);
}

window.i18n = { applyTranslations, initLangSwitcher, get currentLang() { return currentLang; } };